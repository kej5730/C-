// 1. 포인터 변수를 선언할 때. 예) int* ptr1; 은 정수형 변수를 가리키는 포인터 변수 ptr1을 선언

// 2. 포인터가 가리키는 값을 사용할 때 : 이미 선언된 포인터 변수를 사용하여 해당 포인터가 가리키는
											// 메모리 주소에 저당된 값을 읽거나 변경하려면 *을 사용한다.

#include<stdio.h>
#include<stdlib.h>

int main()
{
	int n; // 배열의 크기를 입력받을 함수
	int* arr; // 동적으로 할당할 배열을 가리킬 포인터 변수
	int max, min; // 최대값과 최소값을 저장할 변수

	// 배열의 크기를 입력받기
	printf("배열의 크기를 입력하세요 : ");
	scanf_s("%d", &n);

	arr = (int*)malloc(sizeof(int) * n);

	// 배열 요소 입력 받기
	printf("배열의 요소를 입력하시오 : ");
	for (int i = 0; i < n; i++)
	{
		scanf_s("%d", &arr[i]);
	}

	max = arr[0];
	//배열에서 최대값과 최소값 찾기
	for (int i = 0; i < n; i++)
	{
		if (max < arr[i]) {
			max = arr[i];
		}
		min = arr[0];
		if (min > arr[i])
		{
			min = arr[i];
		}
	}

	//결과 출력
	printf("최대값 : %d, 최소값 : %d", max, min);

	//동적으로 할당한 메모리 해제
	free(arr);
}